# int
age = 25
print(age, type(age))

# float
price = 125.44
print(price, type(price))

# string
name = "subha"
print(name, type(name))

# boolean
is_active = True
print(is_active, type(is_active))

# null
address = None
print(address, type(address))

# dynamic typing or intellisense
x=10
x= 10.14
x= "subha"
print(x,type(x)) 
# python overriding the value and python automatically figures out the datatype