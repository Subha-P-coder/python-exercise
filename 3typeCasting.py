# type casting
# Change one datatype into another datatype

x = '10'
y = '3'

print(type(x))
print(type(y))
print(type(int(x)))
print(type(int(y)))
print(int(x)+int(y))

a = 'A'
b = 'B'
print(int(a)) # we can't change into integer because a, b are not numbers

z = 10.5
print(int(z))
print(float(z))

